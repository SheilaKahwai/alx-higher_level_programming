#!/usr/bin/python3
import json
"""Module 6-load_from_json_file
creates an Object from JSON file.
"""


def load_from_json_file(filename):
    """creates a python object from a
    json file.
    Args:
        -filename: name of json file
    Returns: python object
    """
    with open(filename, 'r') as f:
        obj = json.load(f)
    return obj
